def task_1():
    """Вивести всі парні числа від 1 до 20"""
    print("Парні числа від 1 до 20:")
    for i in range(1, 21):
        if i % 2 == 0:
            print(i, end=' ')
    print()

def task_2():
    """Обчислити суму чисел від 1 до 100"""
    total = 0
    for i in range(1, 101):
        total += i
    print(f"Сума чисел від 1 до 100: {total}")
    # Альтернативний спосіб
    print(f"Перевірка (формула Гауса): {100*101//2}")

def task_3():
    """Вивести таблицю множення для числа 7"""
    print("Таблиця множення для числа 7:")
    for i in range(1, 11):
        print(f"7 x {i} = {7*i}")

def task_4():
    """Знайти факторіал числа за допомогою циклу"""
    n = 5
    factorial = 1
    for i in range(1, n+1):
        factorial *= i
    print(f"{n}! = {factorial}")

def task_5():
    """Вивести числа Фібоначчі до 20-го елемента"""
    a, b = 0, 1
    print("Перші 20 чисел Фібоначчі:")
    for i in range(20):
        print(a, end=' ')
        a, b = b, a + b
    print()

def task_6():
    """Перевернути рядок за допомогою циклу"""
    text = "Python Programming"
    reversed_text = ""
    for char in text:
        reversed_text = char + reversed_text
    print(f"Оригінал: {text}")
    print(f"Перевернутий: {reversed_text}")

def task_7():
    """Знайти всі дільники числа"""
    num = 28
    divisors = []
    for i in range(1, num+1):
        if num % i == 0:
            divisors.append(i)
    print(f"Дільники числа {num}: {divisors}")

def task_8():
    """Перевірити, чи є рядок паліндромом"""
    word = "радар"
    is_palindrome = True
    for i in range(len(word)//2):
        if word[i] != word[-i-1]:
            is_palindrome = False
            break
    print(f"Слово '{word}' є паліндромом: {is_palindrome}")

def task_9():
    """Порахувати кількість голосних літер у рядку"""
    text = "Програмування на Python"
    vowels = "аеиіоуяюєїАЕИІОУЯЮЄЇ"
    count = 0
    for char in text:
        if char in vowels:
            count += 1
    print(f"Текст: '{text}'")
    print(f"Кількість голосних: {count}")

